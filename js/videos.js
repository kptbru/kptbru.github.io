const tabs = document.querySelectorAll('.tabsWrap a');
const sections = document.querySelectorAll('.tabsWrap section');

for(let i=0; i<tabs.length; i++){
  tabs[i].addEventListener('click',function showTabs(e){
      // otmena standartnogo sobitia
      e.preventDefault();
      //console.log(i)
      for(let j=0; j<sections.length; j++){
          if(i == j){
              sections[j].style.display = "block";
          }
          else{
              sections[j].style.display = "none";
          }
      }
  });
}

function collect(selector, collection) {
    let content = collection.map(block => `
    <div class='vidik'>
        <h4>${block.name}</h4>
        <br>
        <iframe width="500" height="380" src="https://www.youtube.com/embed/${block.source}" frameborder="0" allow="accelerometer; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
    </div>
    <br>
    `);
    let block = document.querySelector(selector);
    block.innerHTML = content.join('');
    return;
}

